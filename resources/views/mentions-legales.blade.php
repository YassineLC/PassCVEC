            <section>
                <h2>Règlement (UE) 2016/679 du Parlement européen et du Conseil du 27 avril 2016</h2>
                <p>Règlement relatif à la protection des personnes physiques à l'égard du traitement des données à caractère personnel et à la libre circulation de ces données et abrogeant la directive 95/46/CE (règlement général sur la protection des données). (Pour plus d'informations, cliquez <a href="https://eur-lex.europa.eu/legal-content/FR/TXT/PDF/?uri=CELEX:32016R0679">ici</a>)</p>
            </section>
            <section>
                <h2>Objet du traitement</h2>
                <p>PASS CVEC est une application web permettant aux étudiants de réaliser leur demande de Pass CVEC. Le Pass CVEC est un carnet de 5 tickets offerts aux étudiants de l’académie (et aux étudiants des résidences du Crous de Versailles même s’ils dépendent d’un autre Crous).</p>
            </section>
            <section>
                <h2>Nature du traitement des données personnelles</h2>
                <p><strong>Finalités :</strong> Le traitement a pour objet la gestion des demandes de Pass CVEC et permet de recueillir de façon directe les données personnelles nécessaires au service culture et vie de campus (CVC) du Crous de Versailles. Les étudiants cliquent sur un lien qui leur permet de renseigner un formulaire en ligne. Le formulaire recueille les informations suivantes :</p>
                <ul>
                    <li>Nom Prénom</li>
                    <li>INE</li>
                    <li>Adresse de messagerie</li>
                    <li>Adresse postale</li>
                    <li>Résident Crous oui/non</li>
                    <li>Un certificat de scolarité</li>
                    <li>Une attestation de paiement CVEC</li>
                </ul>
                <p><strong>Base légale :</strong> Ce traitement de données relève de la mission de service public dont est investi le réseau des Crous. Article 6-e du règlement général sur la protection des données – RGPD : e) le traitement est nécessaire à l'exécution d'une mission d'intérêt public ou relevant de l'exercice de l'autorité publique dont est investi le responsable du traitement.</p>
            </section>
            <section>
