                <h2>Données traitées</h2>
                <p><strong>Catégories de données traitées :</strong> Données d’identification, informations d’ordre personnelles.</p>
                <p><strong>Caractère obligatoire du recueil des données :</strong> Le service nécessite sauf mention contraire le recueil obligatoire de ces données qui sont nécessaires à la gestion des demandes et à la délivrance des Pass CVEC.</p>
                <p><strong>Personnes Concernées :</strong> Le traitement de données concerne les personnes physiques étudiantes.</p>
            </section>
            <section>
                <h2>Destinataires des Données Personnelles</h2>
                <p><strong>Catégories de destinataires :</strong> En fonction de leurs besoins respectifs, sont destinataires de tout ou partie des données :</p>
                <ul>
                    <li>Le service CVC du Crous de Versailles</li>
                    <li>La DSI du Crous de Versailles</li>
                </ul>
            </section>
            <section>
                <h2>Transfert des données hors UE</h2>
                <p>Aucun transfert de données hors de l’Union européenne n’est réalisé.</p>
            </section>
            <section>
                <h2>Conservation des Données Personnelles</h2>
                <p>Les informations recueillies relatives à la traçabilité des accès sont conservées pendant toute l’année universitaire en cours. Après cette date, les données collectées seront supprimées soit au plus tard le 30/09.</p>
            </section>
            <section>
                <h2>Sécurité</h2>
                <p>En tant que responsable de traitements, le Crous met en œuvre des techniques et solutions organisationnelles appropriées pour protéger les données personnelles.</p>
            </section>
            <section>
                <h2>Vos droits sur les données vous concernant</h2>
                <p>L’étudiant dispose d'un droit général d'accès, de rectification et de suppression de l'ensemble des données personnelles le concernant qui ont été collectées au titre de son inscription pour bénéficier du service Pass CVEC. Il dispose aussi du droit d’introduire une réclamation auprès de la CNIL.</p>
                <p><strong>Le droit d’accès :</strong> s’effectue directement par « mon profil ». Il permet de visualiser les données personnelles de l’étudiant.</p>
                <p><strong>Concernant le droit de rectification et de suppression :</strong> il devra en faire la demande à <a href="mailto:dpo@crous-versailles.fr">dpo@crous-versailles.fr</a>. Pour que vos demandes soient prises en compte, vous devrez faire parvenir les éléments nécessaires à votre identification à <a href="mailto:dpo@crous-versailles.fr">dpo@crous-versailles.fr</a>, à savoir :</p>
                <ul>
                    <li>Une attestation écrite sur l'honneur par laquelle vous certifiez être le titulaire des dites Données Personnelles</li>
                    <li>Une photocopie d'une pièce d'identité ou de la carte étudiante</li>
                </ul>
            </section>
            <section>
                <h2>Engagement du Crous de Versailles dans le traitement des données personnelles</h2>
                <p>Le Crous de Versailles s’engage à mettre tout en œuvre pour que les données personnelles de l’étudiant soient :</p>
                <ul>
                    <li>Obtenues et traitées loyalement et licitement</li>
                    <li>Collectées pour des finalités déterminées, légitimes et à limiter leur utilisation à ces finalités</li>
                    <li>Adéquates, pertinentes et non excessives par rapport à ces finalités</li>
                    <li>Accessibles et bénéficient d'un droit de rectification ou de suppression suite à une demande adressée par voie postale ou électronique au responsable du traitement précité</li>
                    <li>Conservées conformément aux finalités poursuivies. Les Crous s’engagent à mettre en œuvre les moyens nécessaires pour assurer la sécurité des données en vue d'empêcher qu’elles puissent être endommagées, modifiées, détruites ou communiquées à des tiers non autorisés</li>
                </ul>
            </section>
